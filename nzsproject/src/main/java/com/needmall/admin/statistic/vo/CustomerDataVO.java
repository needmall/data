package com.needmall.admin.statistic.vo;

public class CustomerDataVO {
	private String months;
	private int customerAmount;
	private int activeCustomer;
	private int activeRate;
	private int joinCustomer;	
	private int leaveCustomer;
	public String getMonths() {
		return months;
	}
	public void setMonths(String months) {
		this.months = months;
	}
	public int getCustomerAmount() {
		return customerAmount;
	}
	public void setCustomerAmount(int customerAmount) {
		this.customerAmount = customerAmount;
	}
	public int getActiveCustomer() {
		return activeCustomer;
	}
	public void setActiveCustomer(int activeCustomer) {
		this.activeCustomer = activeCustomer;
	}
	public int getActiveRate() {
		return activeRate;
	}
	public void setActiveRate(int activeRate) {
		this.activeRate = activeRate;
	}
	public int getJoinCustomer() {
		return joinCustomer;
	}
	public void setJoinCustomer(int joinCustomer) {
		this.joinCustomer = joinCustomer;
	}
	public int getLeaveCustomer() {
		return leaveCustomer;
	}
	public void setLeaveCustomer(int leaveCustomer) {
		this.leaveCustomer = leaveCustomer;
	}
	
	
	
	
}
